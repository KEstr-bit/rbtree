#ifndef RB_MAP_H
#define RB_MAP_H

#include "rbtree.h"

/* Структура Map - ассоциативный массив на базе красно-черного дерева
 *
 * Порядок элементов определяется функцией сравнения ключей.
 * Дерево владеет ключами и значениями.
 * Деревья с разными данными не взаимодействуют.
 */
typedef struct {
    RBTree *tree;            /* Внутреннее красно-черное дерево */
} Map;

/*
 * Создание нового ассоциативного массива
 *
 * Выделяет память и создаёт Map поверх красно-черного дерева.
 * Ключи и значения будут принадлежать mapе.
 *
 * cmp - функция сравнения ключей (не NULL)
 * free_key - функция освобождения ключа (не NULL)
 * free_value - функция освобождения значения (не NULL)
 *
 * Возвращает указатель на Map или NULL при ошибке выделения
 *
 * Память должна быть освобождена через destroy_map()
 */
Map *create_map(rb_cmp_fn cmp,
                rb_free_fn free_key,
                rb_free_fn free_value);

/*
 * Уничтожение map
 *
 * Освобождает все ключи, значения и само дерево.
 * После вызова указатель map становится недействительным.
 *
 * map - указатель на Map
 */
void destroy_map(Map *map);

/*
 * Вставка или обновление пары ключ-значение
 *
 * Если ключ уже существует — значение обновляется (старое освобождается).
 * Если ключа нет — добавляется новая пара.
 *
 * map - Map (не NULL)
 * key - ключ (владение передаётся mapе, не должен быть NULL)
 * value - значение (владение передаётся, может быть NULL)
 *
 * Возвращает 1 при успехе, 0 при ошибке
 */
int map_put(Map *map, void *key, void *value);

/*
 * Получение значения по ключу
 *
 * map - Map
 * key - ключ для поиска
 *
 * Возвращает указатель на значение или NULL, если ключ не найден.
 * Не освобождайте возвращённый указатель — он принадлежит mapе.
 */
void *map_get(Map *map, const void *key);

/*
 * Удаление элемента по ключу
 *
 * Освобождает ключ и значение.
 *
 * map - Map
 * key - ключ для удаления
 *
 * Возвращает 1 если элемент был найден и удалён, 0 иначе
 */
int map_remove(Map *map, const void *key);

/*
 * Проверка наличия ключа
 *
 * map - Map
 * key - ключ
 *
 * Возвращает 1 если ключ присутствует, 0 иначе
 */
int does_map_contain(Map *map, const void *key);

/*
 * Получение количества элементов
 */
unsigned int get_map_size(Map *map);

/*
 * Проверка, пуст ли map
 */
int is_map_empty(Map *map);

/*
 * Очистка map (удаляет все элементы, но сам map остаётся)
 */
void clear_map(Map *map);

/*
 * Возвращает узел с минимальным ключом 
 */
RBNode *map_min(Map *map);

/*
 * Возвращает узел с максимальным ключом
 */
RBNode *map_max(Map *map);

#endif /* RB_MAP_H */
